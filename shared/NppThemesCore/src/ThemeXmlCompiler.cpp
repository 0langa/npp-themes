constexpr std::uintmax_t maxThemeBytes = 16U * 1024U * 1024U;

[[nodiscard]] std::string lower(std::string value) {
    std::transform(value.begin(), value.end(), value.begin(),
                   [](unsigned char c) { return static_cast<char>(std::tolower(c)); });
    return value;
}

[[nodiscard]] Color colorFromStyleName(const ThemeProfile& profile, const std::string& styleName) {
    const auto name = lower(styleName);
    if (name.find("comment") != std::string::npos || name.find("remark") != std::string::npos) {
        return profile.palette.comment;
    }
    if (name.find("string") != std::string::npos || name.find("character") != std::string::npos ||
        name.find("quoted") != std::string::npos || name.find("heredoc") != std::string::npos) {
        return profile.palette.string;
    }
    if (name.find("number") != std::string::npos || name.find("numeric") != std::string::npos ||
        name.find("float") != std::string::npos) {
        return profile.palette.number;
    }
    if (name.find("keyword") != std::string::npos || name.find("instruction") != std::string::npos ||
        name.find("reserved") != std::string::npos || name.find("preprocessor") != std::string::npos) {
        return profile.palette.keyword;
    }
    if (name.find("function") != std::string::npos || name.find("method") != std::string::npos ||
        name.find("command") != std::string::npos) {
        return profile.palette.function;
    }
    if (name.find("class") != std::string::npos || name.find("type") != std::string::npos ||
        name.find("interface") != std::string::npos || name.find("structure") != std::string::npos) {
        return profile.palette.type;
    }
    if (name.find("operator") != std::string::npos || name.find("delimiter") != std::string::npos ||
        name.find("symbol") != std::string::npos || name.find("tag") != std::string::npos) {
        return profile.palette.op;
    }
    if (name.find("url") != std::string::npos || name.find("link") != std::string::npos ||
        name.find("active") != std::string::npos) {
        return profile.palette.accent;
    }
    if (name.find("line number") != std::string::npos || name.find("whitespace") != std::string::npos ||
        name.find("indent guide") != std::string::npos || name.find("inactive") != std::string::npos) {
        return profile.palette.muted;
    }
    return profile.palette.foreground;
}

void setColorAttribute(pugi::xml_node node, const char* attributeName, Color color) {
    auto attribute = node.attribute(attributeName);
    if (!attribute) {
        attribute = node.append_attribute(attributeName);
    }
    const auto formatted = formatHexColor(color).substr(1);
    attribute.set_value(formatted.c_str());
}

void setStringAttribute(pugi::xml_node node, const char* attributeName, const std::string& value) {
    auto attribute = node.attribute(attributeName);
    if (!attribute) {
        attribute = node.append_attribute(attributeName);
    }
    attribute.set_value(value.c_str());
}

