ThemeCompileResult compileThemeXml(const std::filesystem::path& source, const std::filesystem::path& destination,
                                   const ThemeProfile& profile) {
    ThemeCompileResult result;
    const auto validation = validateProfile(profile);
    if (!validation.empty()) {
        result.error = validation.front();
        return result;
    }

    std::error_code error;
    const auto sourceSize = std::filesystem::file_size(source, error);
    if (error || sourceSize > maxThemeBytes) {
        result.error = error ? "Unable to inspect source theme" : "Source theme exceeds 16 MiB limit";
        return result;
    }
    if (std::filesystem::equivalent(source, destination, error) && !error) {
        result.error = "Source and destination theme paths must differ";
        return result;
    }

    pugi::xml_document document;
    const auto loaded = document.load_file(source.c_str(), pugi::parse_default, pugi::encoding_utf8);
    if (!loaded) {
        result.error = std::string("Unable to parse source theme: ") + loaded.description();
        return result;
    }
    const auto root = document.child("NotepadPlus");
    if (!root || !root.child("LexerStyles") || !root.child("GlobalStyles")) {
        result.error = "Source theme is missing required NotepadPlus style sections";
        return result;
    }
    const std::string generator =
        "Generated by NppThemes; profile=" + profile.id + "; schema=" + std::to_string(profile.schemaVersion);
    document.prepend_child(pugi::node_comment).set_value(generator.c_str());

    for (auto style : document.select_nodes("/NotepadPlus/GlobalStyles/WidgetStyle")) {
        auto node = style.node();
        const std::string name = node.attribute("name").as_string();
        setColorAttribute(node, "fgColor", colorFromStyleName(profile, name));
        setColorAttribute(node, "bgColor", profile.palette.background);
        if (lower(name) == "default style") {
            setStringAttribute(node, "fontName", profile.fontFamily);
            setStringAttribute(node, "fontSize", std::to_string(profile.fontSizePt));
        }
        ++result.stylesUpdated;
    }

    for (auto lexer : document.select_nodes("/NotepadPlus/LexerStyles/LexerType")) {
        const std::string language = lexer.node().attribute("name").as_string();
        for (auto style = lexer.node().child("WordsStyle"); style; style = style.next_sibling("WordsStyle")) {
            const std::string name = style.attribute("name").as_string();
            const int styleId = style.attribute("styleID").as_int();
            Color foreground = colorFromStyleName(profile, name);
            if (foreground == profile.palette.foreground) {
                foreground = semanticColorForStyle(profile, language, styleId, foreground);
            }
            setColorAttribute(style, "fgColor", foreground);
            setColorAttribute(style, "bgColor", profile.palette.background);
            ++result.stylesUpdated;
        }
    }

    error.clear();
    std::filesystem::create_directories(destination.parent_path(), error);
    if (error) {
        result.error = "Unable to create destination directory: " + error.message();
        return result;
    }

    auto temporary = destination;
    temporary += L".tmp";
    if (!document.save_file(temporary.c_str(), "    ", pugi::format_default, pugi::encoding_utf8)) {
        result.error = "Unable to write temporary theme file";
        return result;
    }
    pugi::xml_document verification;
    const auto verified = verification.load_file(temporary.c_str(), pugi::parse_default, pugi::encoding_utf8);
    if (!verified || !verification.child("NotepadPlus")) {
        result.error = "Generated theme failed post-write validation";
        std::filesystem::remove(temporary, error);
        return result;
    }

#ifdef _WIN32
    if (!::MoveFileExW(temporary.c_str(), destination.c_str(), MOVEFILE_REPLACE_EXISTING | MOVEFILE_WRITE_THROUGH)) {
        result.error = "Unable to atomically install generated theme";
        std::filesystem::remove(temporary, error);
        return result;
    }
#else
    std::filesystem::rename(temporary, destination, error);
    if (error) {
        result.error = "Unable to atomically install generated theme: " + error.message();
        std::filesystem::remove(temporary, error);
        return result;
    }
#endif

    result.success = true;
    return result;
}

